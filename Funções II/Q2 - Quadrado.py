# Definindo uma função lambda que recebe um número (x) e retorna o quadrado dele
quadrado = lambda x: x**2

# Exemplo de uso
# Definindo um número para calcular o quadrado
numero = int(input("Digite um número: "))

# Chamando a função lambda para calcular o quadrado do número
resultado = quadrado(numero)

# Imprimindo o resultado
print(resultado)
